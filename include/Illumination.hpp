        glm::vec3 colorHigh(1.0f, 1.0f, 1.0f);
        glm::vec3 colorMid;
        glm::vec3 colorLow;
        colorMid = colorHigh * glm::vec3(0.5f);
        colorLow = colorMid * glm::vec3(0.2f);

        lightingShader.use();
        lightingShader.setVec3("light.position", lightPos);
        lightingShader.setVec3("viewPos", camera.Position);

        // 设置phong氏光照属性
        lightingShader.setVec3("light.ambient", colorLow);
        lightingShader.setVec3("light.diffuse", colorMid);
        lightingShader.setVec3("light.specular", colorHigh);

